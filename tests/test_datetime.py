import sys

from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent))

import app.libs.data as data

# Exemplos de uso
exemplos = [
    ('15/01/2024', '10:30'),
    ('15/01/2024', '10:30:45'),
    ('15/01/2024', '10:30:45.1'),
    ('15/01/2024', '10:30:45.12'),
    ('15/01/2024', '10:30:45.123'),
    ('15/01/2024', '10:30:45.1234'),
    ('15/01/2024', '10:30:45.12345'),
    ('15/01/2024', '10:30:45.123456'),
]

print('Conversões de Data/Hora com Microssegundos Variáveis')
print('=' * 70)

for exemplo_data, exemplo_hora in exemplos:
    dt = data.convert_to_datetime(exemplo_data, exemplo_hora)
    print(f'{exemplo_data} {exemplo_hora:20s} → {dt} (µs: {dt.microsecond})')

# **Saída:**
# Conversões de Data/Hora com Microssegundos Variáveis
# ======================================================================
# 15/01/2024 10:30              → 2024-01-15 10:30:00 (µs: 0)
# 15/01/2024 10:30:45           → 2024-01-15 10:30:45 (µs: 0)
# 15/01/2024 10:30:45.1         → 2024-01-15 10:30:45.100000 (µs: 100000)
# 15/01/2024 10:30:45.12        → 2024-01-15 10:30:45.120000 (µs: 120000)
# 15/01/2024 10:30:45.123       → 2024-01-15 10:30:45.123000 (µs: 123000)
# 15/01/2024 10:30:45.1234      → 2024-01-15 10:30:45.123400 (µs: 123400)
# 15/01/2024 10:30:45.12345     → 2024-01-15 10:30:45.123450 (µs: 123450)
# 15/01/2024 10:30:45.123456    → 2024-01-15 10:30:45.123456 (µs: 123456)